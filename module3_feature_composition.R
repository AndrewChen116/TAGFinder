#!/usr/bin/env Rscript

################################################################################
# Module 3: Feature composition extraction from persistent homology features
#
# Purpose:
#   Link each persistent homology interval from Module 2 back to the biological
#   features in the Module 1 filtration matrix. For each target topological
#   feature, this module estimates birth composition, death composition, and
#   total feature composition. The output is the direct input for Module 4
#   participation score calculation.
#
# Expected inputs:
#   1) Module 1 output manifest:
#      <prefix>_module1_output_manifest.tsv
#      Required columns: phenotype, safe_phenotype_label, topPct_matrix,
#                        topPct_numeric_matrix
#
#   2) Module 2 input manifest:
#      <prefix>_module3_input_manifest.tsv
#      Required columns: matrix_id, matrix_path, barcode_table
#
#   3) Module 2 barcode tables generated by module2_persistent_homology.R
#
# Main outputs:
#   1) Per-phenotype/matrix composition tables
#   2) Combined composition table
#   3) Composition extraction summary
#   4) Module 4 input manifest
#
# Design principles:
#   - no hard-coded paths
#   - compatible with generic phenotype labels, not cancer-specific labels
#   - standardized input/output schemas across modules
#   - explicit validation and informative error messages
#   - modular functions to avoid copy-and-paste
################################################################################

options(stringsAsFactors = FALSE)

##### 1. Lightweight command-line parser ######################################

parse_args <- function(argv) {
  args <- list()
  i <- 1
  while (i <= length(argv)) {
    key <- argv[[i]]
    if (!startsWith(key, "--")) {
      stop("Unexpected argument: ", key, "\nArguments must use --key value format.", call. = FALSE)
    }
    key <- sub("^--", "", key)
    if (i == length(argv) || startsWith(argv[[i + 1]], "--")) {
      args[[key]] <- TRUE
      i <- i + 1
    } else {
      args[[key]] <- argv[[i + 1]]
      i <- i + 2
    }
  }
  args
}

get_arg <- function(args, name, default = NULL, required = FALSE) {
  value <- args[[name]]
  if (is.null(value)) {
    if (required) stop("Missing required argument: --", name, call. = FALSE)
    return(default)
  }
  value
}

as_logical_arg <- function(x) {
  if (is.logical(x)) return(x)
  x <- tolower(as.character(x))
  if (x %in% c("true", "t", "1", "yes", "y")) return(TRUE)
  if (x %in% c("false", "f", "0", "no", "n")) return(FALSE)
  stop("Cannot parse logical value: ", x, call. = FALSE)
}

